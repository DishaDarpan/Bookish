namespace bookish
{
    public class Book
    {
        public int id { get; set; }
        public string Title { get; set; }
        public string Author { get; set; }
        public string ImageUrl { get; set; }

        public Book(string title, string author, string imageUrl)
        {
            Title = title;
            Author = author;
            ImageUrl = imageUrl;
        }
    }
}
